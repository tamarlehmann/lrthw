# prints line of text
puts "Is it true that 3 + 2 < 5 - 7?"

# prints boolean result of the expression, false
puts 3.0 + 2.0 < 5.0 - 7.0

# prints text with calculated sums inside the #{}
puts "What is 3 + 2? #{3.0 + 2.0}"
puts "What is 5 - 7? #{5.0 - 7.0}"

# prints line of text
puts "Oh, that's why it's false."

# prints line of text
puts "How about some more."

# prints line of text followed by boolean as a result of comparison operators
puts "Is it greater? #{5.0 > -2.0}" #true
puts "Is it greater or equal? #{5.0 >= -2.0}" #true
puts "Is it less or equal? #{5.0 <= -2.0}" #false
